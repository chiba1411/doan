     public void find(HoaDon hd) {
        txtmahd.setText(hd.getMahoadon());
        txtngayxuathd.setText(date.format(hd.getNgayxuat()));
        txttenkh.setText(hd.getTenkh());
        txttencr.setText(hd.getTensp());
        txtgia.setText(Double.toString(hd.getGia()));
        txtsoluong.setText(Double.toString(hd.getSoluong()));

    }
    private void btntkhdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntkhdActionPerformed
        // TODO add your handling code here:
        if(txtmahd.getText().isEmpty()){
            jlbtc.setText("Chưa Nhập");
        }else{
            HoaDon h = frhd.find(txtmahd.getText());
            if(h!=null){
                find(h);
            }else{
                JOptionPane.showMessageDialog(this, "Không Tìm Thấy");
            }
        }
    }//GEN-LAST:event_btntkhdActionPerformed


    public void Tablehd(){
        DefaultTableModel model = (DefaultTableModel) tbhd.getModel();
         model.setRowCount(0);
        for (HoaDon hd : frhd.getallHoaDon()) {
            Object[] rowData = new Object[7];
            rowData[0] = hd.getMahoadon();
            rowData[1] = date.format(hd.getNgayxuat());
            rowData[2] = hd.getTenkh();
            rowData[3] = hd.getTensp();
            rowData[4] = hd.getGia();
            rowData[5] = hd.getSoluong();
            rowData[6] = hd.toString();
            //up table
            model.addRow(rowData);
        }
    }

